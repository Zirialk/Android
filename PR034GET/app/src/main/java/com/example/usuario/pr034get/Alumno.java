package com.example.usuario.pr034get;


public class Alumno {
    boolean repetidor;
    int edad;
    String direccion;
    String telefono;
    String curso;
    String nombre;
    String foto;

    public Alumno(boolean repetidor, int edad, String direccion, String telefono, String curso, String nombre, String foto) {
        this.repetidor = repetidor;
        this.edad = edad;
        this.direccion = direccion;
        this.telefono = telefono;
        this.curso = curso;
        this.nombre = nombre;
        this.foto = foto;
    }


}
